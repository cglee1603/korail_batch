-- ========================================
-- DB의 텍스트 데이터를 RAGFlow에 업로드하기 위한 쿼리입니다.
-- 실제 사용 시 테이블명과 컬럼명을 프로젝트에 맞게 수정하세요.

-- ========================================
-- 기본 예시: 규정/매뉴얼 조회
-- ========================================

SELECT 
    규정ID AS regulation_id,
    규정명 AS title,
    규정내용 AS content,
    적용부서 AS department,
    개정일자 AS revision_date,
    담당자 AS manager
FROM 
    규정관리
WHERE 
    시행여부 = 'Y'
ORDER BY 
    개정일자 DESC;


